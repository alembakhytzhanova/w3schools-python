#Join Two Tuples

#Для объединения двух или более кортежей можно использовать + оператор:
tuple1 = ("a", "b" , "c")
tuple2 = (1, 2, 3)

tuple3 = tuple1 + tuple2
print(tuple3)

#Multiply Tuples

#Если вы хотите умножить содержимое кортежа заданное количество раз, вы можете использовать оператор * :
fruits = ("apple", "banana", "cherry")
mytuple = fruits * 2

print(mytuple)